package product;


import java.io.Serializable;

public class Ingridient implements Serializable{
    private String name;
    private int price;
    private int count;

    public Ingridient() {
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }
}
